//! Self-consistency capture for the AArch64 encoder.
//!
//! Encodes a broad, deterministic sweep of instruction/operand combinations
//! and compares the emitted bytes against values recorded from the current
//! encoder. This is the safety net for behavior-preserving refactors of
//! `src/aarch64/`: any change in emitted bytes or in error reporting flips
//! the per-instruction hashes below.
//!
//! The sweep is driven by `InstId` (0..Count) x a fixed list of operand-shape
//! templates, with candidate operands picked by a mixed-radix digit stream so
//! every shape is exercised with varying registers/immediates. Both the plain
//! id and a condition-code variant are emitted for every combination.
//!
//! After an *intentional* behavior change, regenerate the table with:
//!
//! ```sh
//! ASMKIT_A64_SELFCHECK_UPDATE=1 cargo test --features validation --test aarch64_selfcheck 2>&1 | grep -A100000 SELF_CHECK_TABLE_BEGIN
//! ```
#![cfg(all(feature = "aarch64", feature = "validation"))]

