/// Operand classes used by the shape templates.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
enum Class {
    /// 32-bit GP registers (incl. wsp/wzr).
    W,
    /// 64-bit GP registers (incl. sp/xzr).
    X,
    /// Scalar SIMD&FP registers (b/h/s/d/q).
    Vb,
    Vh,
    Vs,
    Vd,
    Vq,
    /// Vector registers with an arrangement (v.8b .. v.2d).
    VA,
    /// Narrow vector arrangements (sources of long/dot-product forms).
    VA2,
    /// Rotation immediates for fcmla/fcadd (90/270/0/180).
    IR,
    /// Vector registers with an element index (v.b[1], ...).
    VE,
    /// Memory operands (base/offset, pre/post, register/extended index, label).
    M,
    /// Plain immediates.
    I,
    /// Immediates carrying a shift/extend predicate.
    IP,
    /// A label (always bound at offset 0).
    L,
}

