/// Cases emitted per instruction and per condition-code pass.
const CASES_PER_PASS: usize = 2048;

fn gp_candidates() -> Vec<Operand> {
    vec![
        *regs::w(0).as_operand(),
        *regs::w(1).as_operand(),
        *regs::w(2).as_operand(),
        *regs::w(3).as_operand(),
        *regs::w(31).as_operand(), // wsp
        *regs::w(63).as_operand(), // wzr
    ]
}

