/// Error kinds are part of the captured behavior: map them to stable codes.
fn err_code(err: &AsmError) -> u8 {
    match err {
        AsmError::InvalidOperand => 1,
        AsmError::InvalidInstruction => 2,
        AsmError::InvalidImmediate => 3,
        AsmError::TooLarge => 4,
        AsmError::UnsupportedInstruction { .. } => 5,
        _ => 255,
    }
}

fn fnv1a64(mut hash: u64, bytes: &[u8]) -> u64 {
    for &b in bytes {
        hash ^= b as u64;
        hash = hash.wrapping_mul(0x100000001B3);
    }
    hash
}

#[test]
fn selfcheck() {
    let cand_lists: [Vec<Operand>; CLASSES] = [
        candidates(Class::W),
        candidates(Class::X),
        candidates(Class::Vb),
        candidates(Class::Vh),
        candidates(Class::Vs),
        candidates(Class::Vd),
        candidates(Class::Vq),
        candidates(Class::VA),
        candidates(Class::VA2),
        candidates(Class::IR),
        candidates(Class::VE),
        candidates(Class::M),
        candidates(Class::I),
        candidates(Class::IP),
        candidates(Class::L),
    ];

    let cc_bits = (CondCode::NE as u32) << 27; // InstId::ARM_COND shift.
    let inst_count = InstId::_Count as usize;
    let mut hashes = vec![0xCBF29CE484222325u64; inst_count];

    let mut total_cases = 0usize;
    let mut ok_cases = 0usize;
    let mut panic_cases = 0usize;
    let mut ok_insts = 0usize;
    let mut ok_encodings = [false; Encoding::Count as usize];

    // Silence the default panic hook for the duration of the sweep: latent
    // encoder panics on invalid combinations are captured, not fatal.
    let prev_hook = std::panic::take_hook();
    std::panic::set_hook(Box::new(|_| {}));

    for inst in 0..inst_count as u32 {
        let mut inst_ok = false;
        for pass_cc in [0u32, cc_bits] {
            let id_word = inst | pass_cc;
            for case in 0..CASES_PER_PASS {
                let shape = SHAPES[case % SHAPES.len()];
                // First half of the cases: canonical-heavy fillings (digit
                // stream 0 selects the first candidate of every class).
                // Second half: spread the digit stream per instruction so
                // different instructions sample different fillings.
                let mut digits = case / SHAPES.len();
                if case >= CASES_PER_PASS / 2 {
                    digits += inst as usize * 104729;
                }
                let mut ops: Vec<Operand> = Vec::with_capacity(shape.len());
                for class in shape {
                    let list = &cand_lists[*class as usize];
                    ops.push(list[digits % list.len()]);
                    digits /= list.len();
                }

                let outcome = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
                    let mut buf = CodeBuffer::new(Environment::new(Arch::AArch64));
                    let label = buf.get_label();
                    debug_assert_eq!(label.id(), 0, "candidates assume label id 0");
                    buf.bind_label(label);

                    let mut a = Assembler::new(&mut buf);
                    let refs: Vec<&Operand> = ops.iter().collect();
                    a.emit_n(id_word, &refs);
                    (buf.data().to_vec(), buf.error().cloned())
                }));

                total_cases += 1;
                let h = hashes[inst as usize];
                match outcome {
                    Ok((bytes, None)) => {
                        ok_cases += 1;
                        inst_ok = true;
                        hashes[inst as usize] = fnv1a64(fnv1a64(h, &[0]), &bytes);
                    }
                    Ok((_, Some(e))) => {
                        hashes[inst as usize] = fnv1a64(h, &[1, err_code(&e)]);
                    }
                    // The encoder has latent panics on some invalid operand
                    // combinations (e.g. debug-only subtraction overflow in
                    // `check_gp_typex`); they are part of the captured behavior.
                    Err(_) => {
                        panic_cases += 1;
                        hashes[inst as usize] = fnv1a64(h, &[2]);
                    }
                }
            }
        }
        if inst_ok {
            ok_insts += 1;
            let encoding = INST_INFO_TABLE[inst as usize].encoding as usize;
            if encoding < ok_encodings.len() {
                ok_encodings[encoding] = true;
            }
        }
    }

    let _ = std::panic::take_hook();
    std::panic::set_hook(prev_hook);

    let n_ok_encodings = ok_encodings.iter().filter(|&&b| b).count();
    eprintln!(
        "aarch64_selfcheck: {inst_count} instructions, {total_cases} cases: \
         {ok_cases} emitted, {} rejected, {panic_cases} encoder panics; \
         {ok_insts} instructions and {n_ok_encodings}/{} encodings produced \
         at least one instruction",
        total_cases - ok_cases - panic_cases,
        Encoding::Count as usize
    );
    let uncovered: Vec<usize> = ok_encodings
        .iter()
        .enumerate()
        .filter_map(|(i, &b)| (!b).then_some(i))
        .collect();
    eprintln!("  uncovered encodings: {uncovered:?}");

    if std::env::var_os("ASMKIT_A64_SELFCHECK_UPDATE").is_some() {
        eprintln!("SELF_CHECK_TABLE_BEGIN");
        eprintln!("static EXPECTED: &[u64] = &[");
        for h in &hashes {
            eprintln!("    0x{h:016X},");
        }
        eprintln!("];");
        eprintln!("SELF_CHECK_TABLE_END");
        return;
    }

    let mismatches: Vec<usize> = hashes
        .iter()
        .zip(EXPECTED)
        .enumerate()
        .filter_map(|(i, (got, want))| (got != want).then_some(i))
        .collect();
    assert!(
        mismatches.is_empty(),
        "aarch64 self-check: {} instructions changed behavior (first InstIds: {:?}); \
         regenerate with ASMKIT_A64_SELFCHECK_UPDATE=1 if the change was intentional",
        mismatches.len(),
        &mismatches[..mismatches.len().min(16)]
    );
}

